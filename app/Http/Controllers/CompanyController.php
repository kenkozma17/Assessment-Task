    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        // Validation error messages to passed if error occurs
        $errorMsg = [
            'companyAddress.required'   => 'Company Address is required.',
            'companyName.min'           => 'Company Name is required'
        ];

        // Validation object
        $request->validate([
            'companyAddress'  => 'required',
            'companyName'     => 'required'
        ], $errorMsg);

        // Insert query for new company
        $companyInsert = new Company([
            'companyAddress' => $request->post('companyAddress'),
            'companyName'    => $request->post('companyName')
        ]);

        $companyInsert->save();
        return redirect('/')->with('successMsg', 'New Company Successfully Added!');
    }

